'use strict';

class Wine {

  constructor(id, name, wineColor, berries, alcohol, restSugar, acid, bottleSize, price) {
    this.id = id;
    this.name = name;
    this.wineColor = wineColor;
    this.berries = berries;
    this.alcohol = alcohol;
    this.restSugar = restSugar;
    this.acid = acid;
    this.bottleSize = bottleSize;
    this.price = price;
  }

}
